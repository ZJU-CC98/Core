namespace CC98;

/// <summary>
///     提供应用程序设置的默认存放格式。该类型为静态类型。
/// </summary>
public static class AppSettingFormats
{
	/// <summary>
	///     JSON 数据格式。
	/// </summary>
	public const string Json = nameof(Json);

	/// <summary>
	///     XML 数据格式。
	/// </summary>
	public const string Xml = nameof(Xml);

	/// <summary>
	///     二进制数据格式。
	/// </summary>
	public const string Binary = nameof(Binary);
}